
import java.util.*;

public class Java{
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);

        char grade = sc.nextLine().charAt(0);
        switch(grade){
            default:
            System.out.println("i");
            case 'A':
            System.out.println("ii");
            case 'B':
            System.out.println("iii");
            case 'C':
            System.out.println("iiii");
        }
    }
}


